import streamlit as st
import pandas as pd
import joblib

# ==========================================
# Page Configuration
# ==========================================
st.set_page_config(
    page_title="Sales Forecasting App",
    page_icon="📈",
    layout="wide"
)

# ==========================================
# Load Model
# ==========================================
model = joblib.load("models/ridge_model.pkl")
features = joblib.load("models/features.pkl")

# ==========================================
# Sidebar
# ==========================================
st.sidebar.title("📚 Project Information")

st.sidebar.markdown("""
### Research Project

**Title**

Sales Forecasting and Customer Purchase Behaviour Analysis Using Machine Learning

**Researcher**

Roseline Titilope Oni

**Institution**

Lead City University

**Model**

Ridge Regression
""")

# ==========================================
# Main Page
# ==========================================
st.title("🔮 Monthly Sales Prediction")

st.markdown("""
Enter the business metrics below to estimate the expected monthly sales using the trained Ridge Regression model.
""")

st.divider()

# ==========================================
# Input Columns
# ==========================================
col1, col2 = st.columns(2)

with col1:

    year = st.number_input(
        "Year",
        min_value=2024,
        max_value=2035,
        value=2026
    )

    month = st.selectbox(
        "Month",
        list(range(1,13))
    )

    quarter = st.selectbox(
        "Quarter",
        [1,2,3,4]
    )

    total_quantity = st.number_input(
        "Total Quantity Sold",
        min_value=0,
        value=1500
    )

    total_transactions = st.number_input(
        "Number of Transactions",
        min_value=0,
        value=800
    )

with col2:

    lag_1 = st.number_input(
        "Previous Month Sales",
        value=200000.0
    )

    lag_2 = st.number_input(
        "Sales Two Months Ago",
        value=195000.0
    )

    lag_3 = st.number_input(
        "Sales Three Months Ago",
        value=190000.0
    )

    rolling_mean_3 = st.number_input(
        "3-Month Rolling Average",
        value=195000.0
    )

    avg_discount = st.number_input(
        "Average Discount (%)",
        min_value=0.0,
        max_value=100.0,
        value=10.0
    )

    unique_customers = st.number_input(
        "Unique Customers",
        min_value=0,
        value=700
    )

st.divider()

# ==========================================
# Prediction
# ==========================================
if st.button("Predict Monthly Sales", use_container_width=True):

    input_data = pd.DataFrame(
        [[
            year,
            month,
            quarter,
            lag_1,
            lag_2,
            lag_3,
            rolling_mean_3,
            total_quantity,
            avg_discount,
            total_transactions,
            unique_customers
        ]],
        columns=features
    )

    prediction = model.predict(input_data)[0]

    st.success("Prediction completed successfully!")

    st.metric(
        label="Predicted Monthly Sales",
        value=f"${prediction:,.2f}"
    )

    with st.expander("Input Summary"):

        st.dataframe(input_data)

st.divider()

st.info(
    "This prediction is generated by a trained Ridge Regression model and should be used as a decision-support estimate rather than a guaranteed future outcome."
)

st.divider()

st.caption(
    "© 2026 Roseline Titilope Oni | Sales Forecasting using Machine Learning"
)